# Madlibs Game
# word game where you create a story
# by filling in blanks with random words

adjectiveOne = input("Enter an adjective (description): ")
nounOne = input("Enter a noun (person, place, or thing): ")
adjectiveTwo = input("Enter an adjective (description): ")
verbOne = input("Enter a verb ending with ing (action): ")
adjectiveThree = input("Enter an adjective (description): ")

print(f"Today I went to a {adjectiveOne} zoo. ")
print(f"In an exhibit, I saw a {nounOne}.")
print(f"{nounOne} was {adjectiveTwo} and {verbOne}")
print(f"I was {adjectiveThree}!")